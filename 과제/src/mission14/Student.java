package mission14;

public class Student {
	int num;
	String name;
	int score;
	String className;
	
	
	public Student(int num, String name, int score, String className) {
		this.num = num;
		this.name = name;
		this.score = score;
		this.className = className;
	}


	public int getNum() {
		return num;
	}

	public int getScore() {
		return score;
	}	
	
}
